import { readFileSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

// Rebuilds src/lib/daily-challenge-schedule.ts from the daily-eligible pool.
// Days 1..FROZEN_DAYS are preserved verbatim (already published — never rewrite a
// daily players have seen); the rest are filled deterministically from the pool.
//
// Re-run after the catalog changes (the "current"/recency rules mean some future
// entries go stale as players retire). Before re-running, bump FROZEN_DAYS to the
// last challenge number that has already gone live.
//
//   node scripts/generate-daily-schedule.mjs

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");
const catalogPath = path.join(repoRoot, "src/lib/generated-player-catalog.ts");
const schedulePath = path.join(repoRoot, "src/lib/daily-challenge-schedule.ts");

const FROZEN_DAYS = 13; // days 1-13 already published
const TOTAL_DAYS = 1000; // ~2.7 years of puzzles
const SEED = 20260527; // fixed so re-runs are stable
const MIN_STOPS = 3;
const RECENT_SEASON_WINDOW = 10;
const CURRENT_YEAR = new Date().getUTCFullYear();

// ---- eligibility — keep in sync with isDailyEligible() in daily-challenge.ts ----
const isCurrent = (p) => p.careerStatus === "signed" || p.careerStatus === "free_agent";
const careerEndYear = (p) => Math.max(...p.teamStints.map((stint) => (stint.endYear === null ? CURRENT_YEAR : stint.endYear)));
function isEligible(player) {
  if (player.teamStints.length < MIN_STOPS) return false;
  if (player.difficulty === "easy") return true;
  if (player.difficulty === "medium") return isCurrent(player) || careerEndYear(player) >= CURRENT_YEAR - RECENT_SEASON_WINDOW;
  if (player.difficulty === "hard") return isCurrent(player);
  return false;
}

// ---- deterministic shuffle (mulberry32) ----
function mulberry32(seed) {
  let a = seed >>> 0;
  return function () {
    a = (a + 0x6d2b79f5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}
function shuffle(items, rng) {
  const out = [...items];
  for (let i = out.length - 1; i > 0; i -= 1) {
    const j = Math.floor(rng() * (i + 1));
    [out[i], out[j]] = [out[j], out[i]];
  }
  return out;
}

// ---- parse the generated catalog ----
const catalogSrc = readFileSync(catalogPath, "utf8");
const start = catalogSrc.indexOf("[", catalogSrc.indexOf("GENERATED_PLAYERS"));
const end = catalogSrc.indexOf("] as const", start) + 1;
const players = JSON.parse(catalogSrc.slice(start, end));
const eligibleIds = players.filter(isEligible).map((player) => player.id);

// ---- freeze the already-published prefix ----
const scheduleSrc = readFileSync(schedulePath, "utf8");
const existing = [...scheduleSrc.matchAll(/"([^"]+)"/g)].map((match) => match[1]);
const frozen = existing.slice(0, FROZEN_DAYS);
const frozenSet = new Set(frozen);

// Future days draw from eligible players not already used in the frozen prefix,
// cycling through reshuffled copies of the pool (so a player only recurs after a
// full pass) and never repeating back-to-back across a cycle boundary.
const pool = eligibleIds.filter((id) => !frozenSet.has(id));
const rng = mulberry32(SEED);
const future = [];
let lastId = frozen.at(-1) ?? null;
while (frozen.length + future.length < TOTAL_DAYS && pool.length > 0) {
  const cycle = shuffle(pool, rng);
  if (cycle[0] === lastId && cycle.length > 1) [cycle[0], cycle[1]] = [cycle[1], cycle[0]];
  for (const id of cycle) {
    if (frozen.length + future.length >= TOTAL_DAYS) break;
    future.push(id);
    lastId = id;
  }
}

const schedule = [...frozen, ...future];
const body = `// Generated by scripts/generate-daily-schedule.mjs. Do not edit by hand.
// Days 1-${FROZEN_DAYS} are frozen (already published); the rest are filled from the
// daily-eligible pool. Re-run after catalog changes, bumping FROZEN_DAYS first.

export const DAILY_CHALLENGE_SCHEDULE = [
${schedule.map((id) => `  "${id}"`).join(",\n")}
] as const;
`;
writeFileSync(schedulePath, body);
console.log(
  `Wrote ${schedule.length} daily challenges to ${path.relative(repoRoot, schedulePath)} ` +
    `(${frozen.length} frozen + ${future.length} generated from ${eligibleIds.length} eligible, ${pool.length} unique future).`
);
